/**
 * 根据订单ID查找订单
 */
export function findOrderById(id: string): Order | undefined {
  return orders.find((o) => o.id === id);
}

/**
 * 根据驾驶员ID获取订单列表
 */
export function findOrdersByDriver(driverId: string): Order[] {
  return orders
    .filter((o) => o.driverId === driverId)
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());
}

/**
 * 根据业主ID获取订单列表
 */
export function findOrdersByOwner(ownerId: string): Order[] {
  return orders
    .filter((o) => o.ownerId === ownerId)
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());
}

/**
 * 根据车位ID获取订单列表
 */
export function findOrdersByParking(parkingId: string): Order[] {
  return orders
    .filter((o) => o.parkingId === parkingId)
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());
}

/**
 * 根据状态筛选订单
 */
export function filterOrdersByStatus(status: Order['status']): Order[] {
  return orders
    .filter((o) => o.status === status)
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());
}

/**
 * 计算订单总金额统计
 */
export function getOrdersTotalAmount(ordersList: Order[] = orders): number {
  return ordersList.reduce((sum, o) => sum + o.totalAmount, 0);
}

/**
 * 获取订单状态中文描述
 */
export function getOrderStatusText(status: Order['status']): string {
  const statusMap: Record<Order['status'], string> = {
    pending: '待支付',
    paid: '已支付',
    active: '使用中',
    completed: '已完成',
    cancelled: '已取消',
    disputed: '纠纷中',
    refunded: '已退款',
  };
  return statusMap[status];
}

/**
 * 获取支付方式中文描述
 */
export function getPaymentMethodText(method: Order['paymentMethod']): string {
  const methodMap: Record<Order['paymentMethod'], string> = {
    wechat: '微信支付',
    alipay: '支付宝',
    card: '银行卡',
  };
  return methodMap[method];
}
